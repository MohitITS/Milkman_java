    private void displayrecord(int flag) {
        if(framestate==3){
            try {
                if(conn!=null){
                    conn.close();
                }
                conn = methods.getConnection();
                String qry = null;
                if(flag==4){
                   qry = "SELECT * FROM slabfat WHERE ID=(SELECT max(ID) FROM slabfat);";
                }
                if(flag==1){
                   qry = "SELECT * FROM slabfat WHERE ID=(SELECT min(ID) FROM slabfat);";
                }
                if(flag==2){
                   qry = "SELECT * FROM slabfat WHERE ID<"+txtrecordid.getText()+" ORDER BY ID DESC;";
                }
                if(flag==3){
                   qry = "SELECT * FROM slabfat WHERE ID>"+txtrecordid.getText()+" ORDER BY ID;";
                }                
                if(flag==5){
                   qry = "SELECT * FROM slabfat WHERE ID="+txtrecordid.getText()+" ORDER BY ID;";
                }                                
                PreparedStatement selectqry = conn.prepareStatement(qry);
                rs = selectqry.executeQuery();
                
                while(rs.next()){
                    emptyctr();
                    txtrecordid.setText(rs.getString("ID"));
                    txtfromfat.setText(rs.getString("from_fat"));
                    txttofat.setText(rs.getString("to_fat"));
                    txtdiff.setText(rs.getString("diff"));
                    if(rs.getInt("milktype_id")==1) {
                        cmbmilktype.setSelectedIndex(0);
                    } else {
                        cmbmilktype.setSelectedIndex(1);
                    }
                    
                    break;
                }
                rs.close();
                conn.close();
            } catch (SQLException ex) {
                Logger.getLogger(fatslab.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    

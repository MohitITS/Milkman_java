    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try {
            if(conn!=null){
                conn.close();
            }
            conn = methods.getConnection();
            // Insert new Record
            if(framestate==1){
                String insertqry = "INSERT INTO slabfat (from_fat,to_fat,"
                + "diff,milktype_id)"
                + " VALUES (?,?,?,?)";
                PreparedStatement INSERT_QRY = conn.prepareStatement(insertqry);
                INSERT_QRY.setString(1, txtfromfat.getText());
                INSERT_QRY.setString(2, txttofat.getText());
                INSERT_QRY.setString(3, txtdiff.getText());
                if(cmbmilktype.getSelectedIndex()==0){
                    INSERT_QRY.setInt(4, 1);
                } else {
                    INSERT_QRY.setInt(4, 2);
                }

                INSERT_QRY.execute();
            }
            // Update Existing record
            if(framestate==2){
                String updateqry = "UPDATE slabfat SET from_fat = ?, to_fat = ?,"
                + " diff = ?, milktype_id= ?"
                + " WHERE ID = "+txtrecordid.getText()+";";
                PreparedStatement UPDATE_QRY = conn.prepareStatement(updateqry);
                UPDATE_QRY.setString(1, txtfromfat.getText());
                UPDATE_QRY.setString(2, txttofat.getText());
                UPDATE_QRY.setString(3, txtdiff.getText());
                if(cmbmilktype.getSelectedIndex()==0){
                    UPDATE_QRY.setInt(4, 1);
                } else {
                    UPDATE_QRY.setInt(4, 2);
                }
                UPDATE_QRY.execute();
            }
            conn.close();
            String msg = "रेकॉर्ड सेव्ह झाला.";
            JOptionPane.showMessageDialog(null, msg, title, JOptionPane.INFORMATION_MESSAGE);
            framestate=3;
            ED_BUTTONS(false, true);
            displayrecord(4);            
        } catch (SQLException ex) {
            Logger.getLogger(fatslab.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed


    private void loadcombo() {
        try {
            
            cmbkhate1.removeAll();
            cmbkhate2.removeAll();
            cmbpavatiprakar.removeAll();
            
            int i=0;
            
//            if(conn!=null) {
//                conn.close();
//            }
//            conn = methods.getConnection();
            ResultSet rscombo;
            String qry;
            qry = "";
            
            // खाते प्रकार
            qry = "SELECT account.ID, account.accname " +
                    "FROM account " +
                    "ORDER BY account.ID;";
            try (PreparedStatement loadqry = conn.prepareStatement(qry)) {
                rscombo = loadqry.executeQuery();
                khate1id = new int[200];
                khate2id = new int[200];
                
                while(rscombo.next()) {
                   String accname = rscombo.getString("accname");
                   int accid = rscombo.getInt("ID");
                   cmbkhate1.insertItemAt(accname, i);
                   cmbkhate2.insertItemAt(accname, i);
                   khate1id[i] = accid;
                   khate2id[i] = accid;
                   i++;
                }
                cmbkhate1.setSelectedIndex(-1);
                cmbkhate2.setSelectedIndex(-1);
                rscombo.close();
            }

            // Vaoucher Type
            qry = "SELECT vouchertype.ID, vouchertype.vtype " +
                    "FROM vouchertype;";
            try (PreparedStatement ratechartid = conn.prepareStatement(qry)) {
                rscombo = ratechartid.executeQuery();
                pavatiprakarid = new int[10];
                i = 0;
                while(rscombo.next()) {
                   cmbpavatiprakar.insertItemAt(rscombo.getString("vtype"), i);
                   pavatiprakarid[i] = rscombo.getInt("ID");
                   i++;
                }
                rscombo.close();
                cmbpavatiprakar.setSelectedIndex(0);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(jama.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

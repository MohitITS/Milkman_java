    private void displayrecord(int flag) {
        if(framestate==3){
            try {
//                if(conn!=null){
//                    conn.close();
//                }
//                conn = methods.getConnection();
                String qry = null;
//                if (trntype==1) { // jamaa
                    if(flag==4){
                       qry = "SELECT * FROM dailytrn WHERE chalanno=(SELECT max(chalanno) FROM dailytrn);";
                    }
                    if(flag==1){
                       qry = "SELECT * FROM dailytrn WHERE chalanno=(SELECT min(chalanno) FROM dailytrn);";
                    }
                    if(flag==2){
                       qry = "SELECT * FROM dailytrn WHERE chalanno<"+txtchalanno.getText()+" ORDER BY chalanno DESC;";
                    }
                    if(flag==3){
                       qry = "SELECT * FROM dailytrn WHERE chalanno>"+txtchalanno.getText()+" ORDER BY chalanno;";
                    }                
                    if(flag==5){
                       qry = "SELECT * FROM dailytrn WHERE chalanno="+txtchalanno.getText()+";";
                    }                                
//                } else if (trntype==2){ // 
//                    
//                }
                PreparedStatement selectqry = conn.prepareStatement(qry);
                rs = selectqry.executeQuery();
                int i = 0;
                //emptyctr();
                while(rs.next()){

                    txtid.setText(rs.getString("ID"));
                    txtchalanno.setText(""+rs.getInt("chalanno"));
                    dtpdate.setDate(rs.getDate("trndate"));
                    int vtype = rs.getInt("voucherid");
                    for (int j=0;j<cmbpavatiprakar.getItemCount();j++) {
                        if (pavatiprakarid[j]==vtype){
                            cmbpavatiprakar.setSelectedIndex(j);
                            break;
                        }
                    }
                    int acid = rs.getInt("accid");
                    if(i==0) {
                        for (int j=0;j<cmbkhate1.getItemCount();j++) {
                            if (khate1id[j]==acid){
                                cmbkhate1.setSelectedIndex(j);
                                break;
                            }
                        }
                    } else if(i==1) {
                        for (int j=0;j<cmbkhate2.getItemCount();j++) {
                            if (khate2id[j]==acid){
                                cmbkhate2.setSelectedIndex(j);
                                break;
                            }
                        }
                    }
                    if (trntype==1) {
                        txtamount.setText(rs.getString("cramt"));
                    } else if (trntype==2){
                        txtamount.setText(rs.getString("dramt"));
                    }
                    txtparticular.setText(rs.getString("naration"));
                    if (rs.getBoolean("posting")==true) {
                        lblpassunpass.setText("पास");
                    } else {
                        lblpassunpass.setText("अनपास");
                    }

                    i++;
                    if (i>1) break;
                }
                rs.close();
//                conn.close();
            } catch (SQLException ex) {
                Logger.getLogger(producer.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    

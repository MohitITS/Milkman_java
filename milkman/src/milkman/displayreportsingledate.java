    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String cond_qry;
        switch (reporttodispalay) {
            case "Shift Report":
                if (cmbgroup.getSelectedItem()!="ALL") {
                    cond_qry = "mlkCollection.shift_id = "+ shiftid +" and mlkCollection.trn_date = #"+ mdy.format(dtpfromdate.getDate()) +"# and producergroupid = "+cmbgroup.getSelectedItem()+"";
                } else {
                    cond_qry = "mlkCollection.shift_id = "+ shiftid +" and mlkCollection.trn_date = #"+ mdy.format(dtpfromdate.getDate()) +"#";
                }
                try {
                    methods.displayreport("shiftreport", conn, cond_qry);
                } catch (SQLException ex) {
                    Logger.getLogger(milkcollection.class.getName()).log(Level.SEVERE, null, ex);
                }
                break;
            case "reciept":
                if (cmbgroup.getSelectedItem()!="ALL") {
                    cond_qry = "mlkCollection.shift_id = "+ shiftid +" and mlkCollection.trn_date = #"+ mdy.format(dtpfromdate.getDate()) +"# and producergroupid = "+cmbgroup.getSelectedItem()+"";
                } else {
                    cond_qry = "mlkCollection.shift_id = "+ shiftid +" and mlkCollection.trn_date = #"+ mdy.format(dtpfromdate.getDate()) +"#";
                }
                try {
                    methods.displayreport("rptrecieptfullpage", conn, cond_qry); //rptmilkbillreciept
                } catch (SQLException ex) {            
                    Logger.getLogger(milkcollection.class.getName()).log(Level.SEVERE, null, ex);
                }
                break;
            case "Rojkird":
                cond_qry = "dailytrn.trndate = #"+ mdy.format(dtpfromdate.getDate()) +"#";
                try {
                    methods.displayreport("rpt_rojkird", conn, cond_qry); //rptmilkbillreciept
                } catch (SQLException ex) {            
                    Logger.getLogger(milkcollection.class.getName()).log(Level.SEVERE, null, ex);
                }
                break;
        }
    }//GEN-LAST:event_jButton1ActionPerformed


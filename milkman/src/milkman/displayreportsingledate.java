    public displayreportsingledate(String reportname) throws SQLException {
        initComponents();
        reporttodispalay = reportname;
        if(conn!=null){
            conn.close();
        }
        conn = methods.getConnection();
        
        // producer group
        qry = "SELECT producergroup.ID, producergroup.grid" +
                " FROM producergroup;";
        PreparedStatement loadqry = conn.prepareStatement(qry);
        int i = 0;
        try (ResultSet rscombo = loadqry.executeQuery()) {
            grid = new int[30];
            cmbgroup.insertItemAt("ALL", i);
            grid[i] = 0;                
            while(rscombo.next()) {
                i++;
                cmbgroup.insertItemAt(rscombo.getString("grid"), i);
                grid[i] = rscombo.getInt("ID");
            }
        }
        cmbgroup.setSelectedIndex(0);
        
        conn = methods.getConnection();  
        dtpfromdate.setDate(date);   
        optmorning.setSelected(true);
        
        if ("Rojkird".equals(reporttodispalay)) {
            jLabel7.setVisible(false);
            optmorning.setVisible(false);
            optevening.setVisible(false);
        }
    }


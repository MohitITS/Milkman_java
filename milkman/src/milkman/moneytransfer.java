    private void getcashinhandprofitloss() {
        if(!"".equals(txtbankwithdrawamt) || !"".equals(txtanamatrecieved) || !"".equals(txtanamatpaid) || !"".equals(txtexpences)) {
            Double CIH = (Double.parseDouble(txtbankwithdrawamt.getText()) + Double.parseDouble(txtanamatrecieved.getText())) 
                    - (Double.parseDouble(txtpaymentdetail.getText()) + Double.parseDouble(txtexpences.getText()) + Double.parseDouble(txtanamatpaid.getText()));
            txtcashinhand.setText(""+twodf.format(CIH));
            
            Double PL = Double.parseDouble(txttotalamountrecived.getText()) - (Double.parseDouble(txtanamatrecieved.getText()) + 
                    Double.parseDouble(txtpaymentdetail.getText()) + Double.parseDouble(txtanamatpaid.getText()) + 
                    Double.parseDouble(txtexpences.getText()));
            txtprofitloss.setText(""+twodf.format(PL));
        }
    }
    
    private void gettotalpaymentrecieved() {
        if (!"".equals(txttotalmilksendamt.getText()) || !"".equals(txttotalcommisiion.getText()) || !"".equals(txtsharededuct.getText())) {
            double netpaymentrecieved = 0;
            netpaymentrecieved = (Double.parseDouble(txttotalmilksendamt.getText()) + Double.parseDouble(txttotalcommisiion.getText()))
                    - (Double.parseDouble(txtsharededuct.getText()) + Double.parseDouble(txttotalrebatededuct.getText()) + Double.parseDouble(txtotherdeduct.getText()));
            txttotalamountrecived.setText(""+twodf.format(netpaymentrecieved));
            
            getbankopnbal();
        }
    }
    

    private void getbankbal() {
        if (!txtcurrentbankbalance.getText().equals("") || !txtbankwithdrawamt.getText().equals("")) {
            double netpaymentrecieved = 0;
            netpaymentrecieved = Double.parseDouble(txtcurrentbankbalance.getText()) - Double.parseDouble(txtbankwithdrawamt.getText());
            txtbankbalance.setText(""+twodf.format(netpaymentrecieved));
        }        
    }
    
    private void getbankopnbal() {
        try {
            String bankqry = "SELECT moneytransfer.bankbalance, moneytransfer.mtdate" +
                " FROM moneytransfer" +
                " WHERE (((moneytransfer.mtdate)<=#"+ mdy.format(dtptodate.getDate()) +"#))" +
                " ORDER BY moneytransfer.mtdate DESC;";
//            System.out.println(bankqry);
            try (PreparedStatement bankopbal = conn.prepareStatement(bankqry); ResultSet rs_bankopbal = bankopbal.executeQuery()) {
                if(rs_bankopbal.next()) {
                    double bankbal = rs_bankopbal.getDouble("bankbalance");
//                    System.out.println(bankbal);
                    bankbal = bankbal + Double.parseDouble(txttotalamountrecived.getText());
                    txtcurrentbankbalance.setText(""+twodf.format(bankbal));
                } else {
                    txtcurrentbankbalance.setText(""+twodf.format(methods.bankopbal + Double.parseDouble(txttotalamountrecived.getText())));
                }
            }
            getbankbal();
        } catch (SQLException ex) {
            Logger.getLogger(moneytransfer.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    

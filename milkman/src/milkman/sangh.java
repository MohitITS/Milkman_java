    public int getprodid() {
        try {
            int rid;
            String qry;
            qry = "";
            ResultSet rsid;
            if(conn!=null) {
                conn.close();
            }
            conn = methods.getConnection();
            qry = "SELECT Max(sangh.ID) AS MaxOfID\n" +
                    "FROM sangh;";
            PreparedStatement maxid = conn.prepareStatement(qry);
            rsid = maxid.executeQuery();
            while(rsid.next()) {
                rid = rsid.getInt("MaxOfID");
                System.out.println(rid);
                if(rid == 0){
                    id = 1;
                } else {
                    id = rid + 1;
                }
                break;
            }
            rsid.close();
            conn.close();
        } catch (SQLException ex) {
            Logger.getLogger(sangh.class.getName()).log(Level.SEVERE, null, ex);
        }
        return id;
    }
    

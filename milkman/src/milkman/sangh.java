    private void displayrecord(int flag) {
        if(framestate==3){
            try {
                if(conn!=null){
                    conn.close();
                }
                conn = methods.getConnection();
                String qry = null;
                if(flag==4){
                   qry = "SELECT * FROM sangh WHERE ID=(SELECT max(ID) FROM sangh);";
                }
                if(flag==1){
                   qry = "SELECT * FROM sangh WHERE ID=(SELECT min(ID) FROM sangh);";
                }
                if(flag==2){
                   qry = "SELECT * FROM sangh WHERE ID<"+txtsanghid.getText()+" ORDER BY ID DESC;";
                }
                if(flag==3){
                   qry = "SELECT * FROM sangh WHERE ID>"+txtsanghid.getText()+" ORDER BY ID;";
                }                
                if(flag==5){
                   qry = "SELECT * FROM sangh WHERE ID="+txtsanghid.getText()+" ORDER BY ID;";
                }                                
                PreparedStatement selectqry = conn.prepareStatement(qry);
                rs = selectqry.executeQuery();
                
                while(rs.next()){
                    emptyctr();
                    txtsanghid.setText(rs.getString("ID"));
                    String proname = rs.getString("sname");
                    txtsanghname.setText(proname);
                    txtaddress.setText(rs.getString("address"));
                    txtcontacno.setText(rs.getString("cno"));
                    int rn = rs.getInt("rtc_no");
                    int i;
                    for (i=0;i<cmbratechart.getItemCount();i++){
                        String cmbrate = (String) cmbratechart.getItemAt(i);
                        if(cmbrate.equals(""+rn)){
                            cmbratechart.setSelectedIndex(i);
                            break;
                        }
                    }
                    txtcommision.setText(rs.getString("comission"));
                    txtrebate.setText(rs.getString("rebate"));
                    txtshareanamat.setText(rs.getString("shareanamat"));

                    break;
                }
                rs.close();
                conn.close();
            } catch (SQLException ex) {
                Logger.getLogger(sangh.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }    
    
